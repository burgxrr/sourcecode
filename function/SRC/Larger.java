
import java.util.Scanner;

public class Larger{
public static void main(String[] args){

	int a,b;
	Scanner in=new Scanner(System.in);

	System.out.print("enter two numbers: ");
	a=in.nextInt();
	b=in.nextInt();
	larger(a,b);
}

static void larger(int x,int y){

	if(x>y)
	System.out.println
	(x+" is the larger number");
	else
	System.out.println
	(y+" is the larger number");
}
}




